public class BibliotecaTO {
    private String idbiblioteca;
    private String nomedolivro;
    private String autor;
    private String editora;
    private int anodepublicacao;
    private String catalogacao;
    private int edicao;

    public String getIdbiblioteca() {
        return idbiblioteca;
    }

    public void setIdbiblioteca(String idbiblioteca) {
        this.idbiblioteca = idbiblioteca;
    }

    public String getNomedolivro() {
        return nomedolivro;
    }

    public void setNomedolivro(String nomedolivro) {
        this.nomedolivro = nomedolivro;
    }

    public String getAutor() {
        return autor;
    }

    public void setAutor(String autor) {
        this.autor = autor;
    }

    public String getEditora() {
        return editora;
    }

    public void setEditora(String editora) {
        this.editora = editora;
    }

    public int getAnodepublicacao() {
        return anodepublicacao;
    }

    public void setAnodepublicacao(int anodepublicacao) {
        this.anodepublicacao = anodepublicacao;
    }

    public String getCatalogacao() {
        return catalogacao;
    }

    public void setCatalogacao(String catalogacao) {
        this.catalogacao = catalogacao;
    }

    public int getEdicao() {
        return edicao;
    }

    public void setEdicao(int edicao) {
        this.edicao = edicao;
    }

    
   
}
